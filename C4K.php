                <h2>Summary</h2>
                <p>Computers 4 Kids is a Charlottesville makerspace and non-profit that empowers low income youth. They cultivate learning and creativity by giving kids access to technology and mentorship. Unmanaged, their navigation and layout descended into disarray and disorganization, and they needed advice on a new UI.</p><br>
                
                <h2>Role Context:</h2>
                <p>I teamed up with another web designer and two marketing interns to deliver web and marketing consulting to C4K. This was a side project from our hybrid summer training and internship program.</p><br>
                
                <h3>Research and Discovery:</h3>
                <h4>The Insights We Gained</h4>
                <p>On the onset, our prompt at the time was extremely open ended - my team was assigned to provide help to our local non-profit. We eagerly awaited a meeting with Lizzie, the program director at Computers 4 Kids to narrow down what we could specifically do for the organization.</p><br>
                
                <h5>Early Insights</h5>
                <h6>1. C4K needed to visually connect and convey trustworthiness</h6>
                <p>C4K needed a visual appeal that broke down the austerity of their name: Computers 4 Kids. During our discussion, Sarah told us amazing heart felt stories of children that had benefitted from C4K’s opportunities, and we quickly understood that at the core, C4K needed to feel personal to people. The organization needed a welcoming site that helped users understand that C4K existed to serve the families and children which it invited through its doors.

</p><br>
           <p>One of C4K’s fears was that the name “Computers 4 Kids” didn’t accurately represent that organization as a makerspace and instead sounded like an austere or inaccessible STEM resource. Therefore, during rebranding, we made it our goal for C4K to radiate the feeling of welcoming & playfulness. We wanted the visual elements to make people feel invited into a safe and encouraging learning environment.</p><br>
           
           <p>Such an aesthetic was essential in removing people’s hesitations in signing up for C4K’s programs, and make the organization seem more relatable and therefore accessible.</p><br>
